	public static function getArray() {
		return self::$countries[ self::DEFAULT_LANGUAGE ];
	}


	public static function getTranslatedArray() {
		$lang = strtolower( rsCore::$Dictionary->get_language() );
		if( self::$countries[ $lang ] === null ) {
			self::$countries[ $lang ] = self::translateArray( $lang );
		}
		return self::$countries[ $lang ];
	}


	private static function translateArray() {
		$translated = array();
		foreach( self::getArray() as $key => $country ) {
			$translated[ $key ] = rsDictionary::t($country);
		}
		return $translated;
	}


	public static function __callStatic( $name, $params ) {
		$lang = strtolower( rsCore::$Dictionary->get_language() );
		return self::$countries[ $lang ][ $name ];
	}


